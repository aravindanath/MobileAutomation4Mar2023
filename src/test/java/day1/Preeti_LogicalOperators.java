package day1;

public class Preeti_LogicalOperators {
    public static void main(String[] args) {


        int a = 22;
        int b = 2;

        System.out.println(a==b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);

        System.out.println(a!=b);
        System.out.println(a==b && b<a);
        System.out.println(a==b || b<a);
    }
}
