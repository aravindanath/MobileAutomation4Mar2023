
package day1;

public class LogicalOperators {

    public static void main(String[] args) {


        int i = 12;
        int j = 2;

        System.out.println(i==j);
        System.out.println(i<j);
        System.out.println(i>j);
        System.out.println(i<=j);
        System.out.println(i>=j);

        System.out.println(i!=j);
        System.out.println(i==j && j<i);
        System.out.println(i==j || j<i);
    }
}
